def test_board_to_layers():
    board = chess.Board()
    expected = STARTING_LAYERS[:]
    expected.extend((
        np.zeros((8,8)),
        np.zeros((8,8))
    ))
    actual = board_to_layers(board, chess.WHITE)

    np.testing.assert_array_equal(expected, actual)


def test_board_to_layers_repetition_two():
    board = chess.Board()
    board.push(chess.Move.null())
    board.push(chess.Move.null())

    expected = STARTING_LAYERS[:]
    expected.extend((
        np.ones((8,8)),
        np.zeros((8,8))
    ))
    actual = board_to_layers(board, chess.WHITE)

    np.testing.assert_array_equal(expected, actual)


